export async function writeMarkdownEmbedConfig(tempRoot) {
  const configPath = path.join(tempRoot, "diagrampilot.config.yaml");
  const sourcePath = path.join(tempRoot, "docs", "architecture.dp.yaml");
  const svgPath = path.join(tempRoot, "generated", "svg", "architecture.svg");
  const embedPath = path.join(
    tempRoot,
    "generated",
    "embeds",
    "architecture.md",
  );

  await writeFile(
    configPath,
    [
      "version: 1",
      "artifacts:",
      "  - source: docs/architecture.dp.yaml",
      "    outputs:",
      "      - format: svg",
      "        path: generated/svg/{stem}.svg",
      "      - format: markdown",
      "        path: generated/embeds/{stem}.md",
      "",
    ].join("\n"),
    "utf8",
  );
  await writeValidDiagramSource(sourcePath);

  return {
    embedPath,
    svgPath,
  };
}

export async function writeMarkdownEmbedArtifact(embedPath, content) {
  await mkdir(path.dirname(embedPath), { recursive: true });
  await writeFile(embedPath, content, "utf8");
}

export async function writeSvgAndMarkdownEmbed(options) {
  await mkdir(path.dirname(options.svgPath), { recursive: true });
  await writeFreshSvgArtifact(options.svgPath, options.provenanceSourcePath);
  await writeMarkdownEmbedArtifact(options.embedPath, options.embedContent);
}

export function requiredSource(sourcesByPath, sourcePath) {
  const source = sourcesByPath.get(sourcePath);

  assert.notEqual(source, undefined, `Expected result for ${sourcePath}`);

  return source;
}

export function sha256(content) {
  return createHash("sha256").update(content).digest("hex");
}

function assertWorkflowSummary(result, summary) {
  assert.equal(result.ok, true);
  assert.deepEqual(result.summary, summary);
}

export function assertSingleFreshSource(result) {
  assertWorkflowSummary(result, {
    checkedSourceCount: 1,
    freshSourceCount: 1,
    issueCount: 0,
  });
}

export function assertSingleIssueSource(result) {
  assertWorkflowSummary(result, {
    checkedSourceCount: 1,
    freshSourceCount: 0,
    issueCount: 1,
  });
}
